package by.htp.les02.main;

public class SwitchEx {

	public static void main(String[] args) {
		int x = 3;

		switch (x) {// string enum
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		
		case 3:
			System.out.println("3");
			break;
		default:
			System.out.println("default");
			break;
		}
	}

}
